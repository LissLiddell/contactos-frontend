import { useState } from 'react';
import axios from 'axios';
import { useNavigate } from 'react-router-dom';

function Login() {
  const [form, setForm] = useState({ email: '', password: '' });
  const navigate = useNavigate();

  const handleChange = (e) => {
    setForm({ ...form, [e.target.name]: e.target.value });
  };

  const handleSubmit = async (e) => {
    e.preventDefault();
    try {
      const res = await axios.post('http://localhost:5000/api/auth/login', form);
      localStorage.setItem('token', res.data.token);
      navigate('/dashboard');
    } catch (err) {
      alert(err.response?.data?.message || 'Error al iniciar sesi칩n');
    }
  };

  return (
    <div className="container ">
      <h2>Iniciar Sesi칩n</h2>
      <form onSubmit={handleSubmit}>
        <input name="email" placeholder="Correo" onChange={handleChange} required />
        <input name="password" type="password" placeholder="Contrase침a" onChange={handleChange} required />
        <button type="submit">Iniciar sesi칩n</button>
      </form>
    </div>
  );
}

export default Login;